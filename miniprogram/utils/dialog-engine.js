/**
 * 匹配对话规则
 * @param {string} userText 用户输入
 * @returns {{ reply: string, chips: string[] } | null}
 */
function matchRule(userText) {
  const lowerText = userText.toLowerCase();
  for (const rule of DIALOG_RULES) {
    if (rule.keywords.some(kw => lowerText.includes(kw))) {
      return { reply: rule.reply, chips: rule.chips };
    }
  }
  return null;
}

